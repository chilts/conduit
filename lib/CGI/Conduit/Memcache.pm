package CGI::Conduit::Memcache;

use Moose::Role;
use Projectus::Memcache qw(get_memcache);

## ----------------------------------------------------------------------------

has 'memcache_obj' => ( is => 'rw' );

## ----------------------------------------------------------------------------

sub memcache {
    my ($self) = @_;
    return $self->memcache_obj || $self->memcache_obj( get_memcache() );
}

after 'clear' => sub { };

## ----------------------------------------------------------------------------
1;
