## ----------------------------------------------------------------------------

package CGI::Conduit::Log;

use Moose::Role;
use Projectus::Log qw(log_init);

## ----------------------------------------------------------------------------

has 'log_filename' => ( is => 'rw' );

## ----------------------------------------------------------------------------

after 'init' => sub {
    my ($self) = @_;

    log_init( $self->log_filename );
};

## ----------------------------------------------------------------------------
1;
