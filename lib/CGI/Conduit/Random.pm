package CGI::Conduit::Random;

use Moose::Role;
use String::Random qw(random_string);
use String::Random::NiceURL qw(id);

## --------------------------------------------------------------------------------------------------------------------

sub random_salt {
    my ($self, $length) = @_;
    return random_string( 's' x $length );
}

sub random_id {
    my ($self, $length) = @_;
    return id($length);
}

after 'clear' => sub { };

## --------------------------------------------------------------------------------------------------------------------
1;
