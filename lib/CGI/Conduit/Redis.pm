package CGI::Conduit::Redis;

use Moose::Role;
use Projectus::Redis qw(get_redis);

## ----------------------------------------------------------------------------

has 'redis_obj' => ( is => 'rw' );

## ----------------------------------------------------------------------------

sub redis {
    my ($self) = @_;
    return $self->redis_obj || $self->redis_obj( get_redis() );
}

after 'clear' => sub { };

## ----------------------------------------------------------------------------
1;
